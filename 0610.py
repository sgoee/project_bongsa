# ============================================================
# [셀 4] 기초 코드 3: for 반복문으로 평균 직접 계산
# ============================================================
# 수학 시간에 배운 평균을 코드로 직접 만들어보기
총합 = 0
for 점수 in 수학점수들:
    총합 = 총합 + 점수
    # print("현재까지 총합:", 총합)   # 과정 보고 싶으면 주석 해제

평균 = 총합 / len(수학점수들)
print("직접 계산한 평균:", round(평균, 2))



# ============================================================
# [셀 5] 기초 코드 4: 직접 평균을 계산하는 함수 만들기
# ============================================================
def 평균_구하기(점수목록):
    """리스트를 넣으면 평균을 돌려주는 함수"""
    총합 = 0
    for 점수 in 점수목록:
        총합 = 총합 + 점수
    return 총합 / len(점수목록)

print("내가 만든 함수로 계산한 평균:", round(평균_구하기(수학점수들), 2))



